package com.example.springbootproject;

public class ByProp {
    private long id;
    private String tag;

    public ByProp() {}

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }
}
